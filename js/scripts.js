function toggleAbstract(id) {
    var element = document.getElementById(id);
    element.style.display = (element.style.display === 'none') ? 'block' : 'none';
}

function toggleBibtex(id) {
    var element = document.getElementById(id);
    element.style.display = (element.style.display === 'none') ? 'block' : 'none';
}

